public class OperacionesAritmeticas {
    public static void main(String[] args) {
        float resultado;
        resultado = 5*9/4+1-7*(float)Math.pow(4, 2)+5*4%2/3+24-5*3/2;
        System.out.println(resultado);
        resultado = (float)5*3/2;
        System.out.println(resultado);
        resultado = 5*1.0f*3/2;
        System.out.println(resultado);
        resultado = (float)(5*4)%3/3;
        System.out.println(resultado);


    }

}
